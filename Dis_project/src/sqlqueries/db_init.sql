Drop TABLE if EXISTS specs; 

CREATE TABLE specs (Brand VARCHAR(50),
					Model VARCHAR(50),
					Processor_brand VARCHAR(50),
					Processor_name INTEGER,
					Processor_gen INTEGER,
					ram_GB INTEGER,
					ram_type VARCHAR(50),
					ssd VARCHAR(50),
					hdd VARCHAR(50),
					os VARCHAR(50),
					os_bit VARCHAR(50),
					Graphic_card VARCHAR(50),
					Weight INTEGER,
					Display_size VARCHAR(50),
					Warranty VARCHAR(50),
					Touchscreen VARCHAR(50),
					msoffice VARCHAR(50),
					latest_price INTEGER,
					old_price INTEGER,
					discount INTEGER,
					Star_rating INTEGER,
					Ratings INTEGER,
					reviews INTEGER
				   );


